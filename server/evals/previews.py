def _preview_base64(
    grid: list[list[tuple[int, int, int]]], *, cell_size: int = 12
) -> str:
    if Image is None:  # pragma: no cover
        raise RuntimeError("Pillow is required to generate evaluation previews")
    height = len(grid)
    width = len(grid[0])
    image = Image.new("RGB", (width * cell_size, height * cell_size))
    pixels: list[tuple[int, int, int]] = []
    for row in grid:
        expanded_row: list[tuple[int, int, int]] = []
        for color in row:
            expanded_row.extend([color] * cell_size)
        for _ in range(cell_size):
            pixels.extend(expanded_row)
    image.putdata(pixels)
    buffer = io.BytesIO()
    image.save(buffer, format="PNG")
    return base64.b64encode(buffer.getvalue()).decode()
